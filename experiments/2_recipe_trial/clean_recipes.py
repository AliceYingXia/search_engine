"""
clean_recipes.py

For each Workato recipe JSON in example/, generate two cleaned files:
  - example/cleaned/<name>_semantic.json   — content fields for embedding
  - example/cleaned/<name>_tracking.json  — metadata fields for Qdrant payload

Run:
    python clean_recipes.py
"""

import json
import re
import os
import glob
from html.parser import HTMLParser


# ---------------------------------------------------------------------------
# HTML stripping
# ---------------------------------------------------------------------------

class _HTMLStripper(HTMLParser):
    def __init__(self):
        super().__init__()
        self._parts = []

    def handle_data(self, data):
        self._parts.append(data)

    def get_text(self):
        return " ".join(self._parts).strip()


def strip_html(value):
    """Remove HTML tags from a string. Returns None if value is None."""
    if value is None:
        return None
    if not isinstance(value, str):
        return value
    stripper = _HTMLStripper()
    stripper.feed(value)
    return stripper.get_text() or None


# ---------------------------------------------------------------------------
# Data pill resolution
# ---------------------------------------------------------------------------

# Matches #{_dp('...')} — standard interpolation format
_DP_PATTERN = re.compile(r"#\{_dp\('(.+?)'\)\}")
# Matches =_dp('...') — formula mode (whole value is a pill expression)
_DP_FORMULA_PATTERN = re.compile(r"=_dp\('(.+?)'\)")


def _resolve_pill(pill_json_str):
    """Parse a _dp() JSON payload and return a human-readable label."""
    try:
        pill = json.loads(pill_json_str)
    except json.JSONDecodeError:
        return None  # leave raw if unparseable

    pill_type = pill.get("pill_type", "unknown")
    path = pill.get("path", [])

    if pill_type == "output":
        provider = pill.get("provider", "unknown")
        parts = [provider] + path
    elif pill_type == "job_context":
        parts = ["job_context"] + path
    else:
        parts = [pill_type] + path

    return "[" + " > ".join(parts) + "]"


def resolve_pills(value):
    """
    Recursively walk a value (str / dict / list) and replace every
    #{_dp('...')} and =_dp('...') occurrence with a human-readable
    [provider > path] label.
    """
    if isinstance(value, str):
        def _replace(match):
            resolved = _resolve_pill(match.group(1))
            return resolved if resolved is not None else match.group(0)
        value = _DP_PATTERN.sub(_replace, value)
        value = _DP_FORMULA_PATTERN.sub(_replace, value)
        return value
    if isinstance(value, dict):
        return {k: resolve_pills(v) for k, v in value.items()}
    if isinstance(value, list):
        return [resolve_pills(item) for item in value]
    return value


# ---------------------------------------------------------------------------
# Recursive codeline walker
# ---------------------------------------------------------------------------

SEMANTIC_FIELDS = {"as", "keyword", "provider", "name", "title", "comment", "input"}
TRACKING_FIELDS = {"as", "uuid", "number", "keyword", "provider", "name"}


def walk_codelines(codeline, parent_as, depth, semantic_steps, tracking_steps):
    """
    Recursively walk a codeline and its block children.
    Appends one entry per step into semantic_steps and tracking_steps.
    When 'as' is missing (schema violation), a synthetic key '_step_{number}'
    is assigned so children can still trace ancestry through this step.
    Note: 'description' is intentionally excluded — it is auto-generated by
    Workato's UI and is not updated when recipe logic changes.
    """
    step_as = codeline.get("as")
    if step_as is None:
        # Synthetic key preserves the ancestry chain despite the schema violation
        step_as = f"_step_{codeline.get('number', len(tracking_steps))}"

    # --- semantic entry ---
    semantic = {k: codeline[k] for k in SEMANTIC_FIELDS if k in codeline}
    semantic["as"] = step_as  # ensure synthetic key is stored
    if "input" in semantic:
        semantic["input"] = resolve_pills(semantic["input"])
    semantic_steps.append(semantic)

    # --- tracking entry ---
    tracking = {k: codeline[k] for k in TRACKING_FIELDS if k in codeline}
    tracking["as"] = step_as  # ensure synthetic key is stored
    tracking["parent_as"] = parent_as
    tracking["depth"] = depth
    tracking_steps.append(tracking)

    # --- recurse into block ---
    for child in codeline.get("block", []):
        walk_codelines(child, step_as, depth + 1, semantic_steps, tracking_steps)


def build_recipe_summary(recipe_name, recipe_description, config, semantic_steps, tracking_steps):
    """
    Build a static text summary of the recipe for use as a recipe-level chunk.
    Uses provider + name + keyword (not description, which can be stale).
    """
    lines = []
    lines.append(f"Recipe: {recipe_name}")
    if recipe_description:
        lines.append(f"Description: {recipe_description}")

    providers = [c["provider"] for c in config if "provider" in c]
    lines.append(f"Connectors: {', '.join(providers)}")

    lines.append("Steps:")
    for s_step, t_step in zip(semantic_steps, tracking_steps):
        indent = "  " * t_step.get("depth", 0)
        keyword = s_step.get("keyword", "")
        provider = s_step.get("provider", "")
        name = s_step.get("name", "")
        if provider and name:
            lines.append(f"{indent}- {keyword}: {provider} / {name}")
        else:
            lines.append(f"{indent}- {keyword}")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main processing
# ---------------------------------------------------------------------------

def clean_recipe(filepath):
    with open(filepath) as f:
        data = json.load(f)

    flow = data["flow"]
    recipe_name = flow["name"]
    recipe_description = flow.get("description") or None

    semantic_steps = []
    tracking_steps = []
    walk_codelines(flow["code"], parent_as=None, depth=0, semantic_steps=semantic_steps, tracking_steps=tracking_steps)

    recipe_summary = build_recipe_summary(
        recipe_name, recipe_description, flow.get("config", []),
        semantic_steps, tracking_steps
    )

    semantic_out = {
        "recipe_name": recipe_name,
        "recipe_description": recipe_description,
        "recipe_summary": recipe_summary,
        "steps": semantic_steps,
    }

    tracking_out = {
        "recipe_name": recipe_name,
        "config": flow.get("config", []),
        "steps": tracking_steps,
    }

    return semantic_out, tracking_out


def main():
    input_dir = os.path.join(os.path.dirname(__file__), "example")
    output_dir = os.path.join(input_dir, "cleaned")
    os.makedirs(output_dir, exist_ok=True)

    json_files = glob.glob(os.path.join(input_dir, "*.json"))
    if not json_files:
        print("No JSON files found in example/")
        return

    for filepath in sorted(json_files):
        basename = os.path.splitext(os.path.basename(filepath))[0]
        print(f"Processing: {basename}")

        semantic, tracking = clean_recipe(filepath)

        semantic_path = os.path.join(output_dir, f"{basename}_semantic.json")
        tracking_path = os.path.join(output_dir, f"{basename}_tracking.json")

        with open(semantic_path, "w") as f:
            json.dump(semantic, f, indent=2)

        with open(tracking_path, "w") as f:
            json.dump(tracking, f, indent=2)

        print(f"  -> {os.path.relpath(semantic_path)}")
        print(f"  -> {os.path.relpath(tracking_path)}")

    print("\nDone.")


if __name__ == "__main__":
    main()
